function buildChain(n: number): AuditChain {
  let c = createChain();
  for (let i = 0; i < n; i++) {
    ({ chain: c } = appendEntry(c, 'CREATE', 'data', `user${i}`, 'Record', `${i}`));
  }
  return c;
}
