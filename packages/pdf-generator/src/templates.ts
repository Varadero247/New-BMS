/**
 * Generate HTML for an invoice.
 */
export function invoiceTemplate(data: InvoiceData): string {
  const itemRows = data.items
    .map(
      (item) => `
      <tr>
        <td>${item.description}</td>
        <td style="text-align: center">${item.quantity}</td>
        <td style="text-align: right">${formatCurrencyValue(item.unitPrice, data.currency)}</td>
        <td style="text-align: right">${formatCurrencyValue(item.total, data.currency)}</td>
      </tr>`,
    )
    .join('');

  return `<!DOCTYPE html>
<html>
<head><meta charset="utf-8">${STYLES}</head>
<body>
<div class="page">
  <div class="header">
    <div>
      <h1>INVOICE</h1>
      <div class="subtitle">${data.invoiceNumber}</div>
    </div>
    <div class="company-info">
      <strong>${data.company.name}</strong><br>
      ${data.company.address.replace(/\n/g, '<br>')}
      ${data.company.taxId ? `<br>Tax ID: ${data.company.taxId}` : ''}
    </div>
  </div>

  <div class="section">
    <div class="kv-pair"><span class="kv-key">Invoice Date:</span><span>${data.date}</span></div>
    <div class="kv-pair"><span class="kv-key">Due Date:</span><span>${data.dueDate}</span></div>
    <div class="kv-pair"><span class="kv-key">Bill To:</span><span>${data.customer.name}<br>${data.customer.address.replace(/\n/g, '<br>')}</span></div>
  </div>

  <table>
    <thead>
      <tr>
        <th>Description</th>
        <th style="text-align: center">Qty</th>
        <th style="text-align: right">Unit Price</th>
        <th style="text-align: right">Total</th>
      </tr>
    </thead>
    <tbody>${itemRows}</tbody>
  </table>

  <div class="totals">
    <div class="total-row"><span>Subtotal:</span><span>${formatCurrencyValue(data.subtotal, data.currency)}</span></div>
    <div class="total-row"><span>Tax (${data.taxRate}%):</span><span>${formatCurrencyValue(data.taxAmount, data.currency)}</span></div>
    <div class="total-row grand-total"><span>Total:</span><span>${formatCurrencyValue(data.total, data.currency)}</span></div>
  </div>

  ${data.notes ? `<div class="section"><h2>Notes</h2><p>${data.notes}</p></div>` : ''}

  <div class="footer">
    <p>Generated by IMS Finance Module</p>
  </div>
</div>
</body>
</html>`;
}

/**
 * Generate HTML for a report.
 */
export function reportTemplate(data: ReportData): string {
  const sections = data.sections
    .map(
      (section) => `
      <div class="section">
        <h2>${section.heading}</h2>
        <p>${section.content}</p>
        ${
          section.metrics
            ? `<div style="margin-top: 10px">${section.metrics
                .map(
                  (m) => `<div class="metric"><div class="metric-value">${m.value}</div><div class="metric-label">${m.label}</div></div>`,
                )
                .join('')}</div>`
            : ''
        }
      </div>`,
    )
    .join('');

  return `<!DOCTYPE html>
<html>
<head><meta charset="utf-8">${STYLES}</head>
<body>
<div class="page">
  <div class="header">
    <div>
      <h1>${data.title}</h1>
      <div class="subtitle">Period: ${data.period}</div>
    </div>
    <div class="company-info">
      Report Date: ${data.reportDate}<br>
      Author: ${data.author}
    </div>
  </div>

  ${sections}

  ${data.summary ? `<div class="section"><h2>Summary</h2><p>${data.summary}</p></div>` : ''}

  <div class="footer">
    <p>Generated by IMS Reporting Engine</p>
  </div>
</div>
</body>
</html>`;
}

