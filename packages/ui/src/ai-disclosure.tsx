'use client';

import * as React from 'react';
import { cn } from './utils';

export interface AIDisclosureProps {
  /** Name of the AI system producing the output */
  systemName: string;
  /** Confidence score 0-1 (displayed as percentage) */
  confidence?: number;
  /** Model / version identifier */
  modelVersion?: string;
  /** When the output was generated */
  generatedAt?: Date | string;
  /** Short explanation of how the AI produced this output */
  reasoning?: string;
  /** Whether the output is under human review */
  pendingReview?: boolean;
  /** Variant styling */
  variant?: 'inline' | 'banner' | 'compact';
  className?: string;
}

export function AIDisclosure({
  systemName,
  confidence,
  modelVersion,
  generatedAt,
  reasoning,
  pendingReview,
  variant = 'inline',
  className,
}: AIDisclosureProps) {
  const [expanded, setExpanded] = React.useState(false);
  const confidencePct = confidence != null ? Math.round(confidence * 100) : null;

  const formattedDate = generatedAt
    ? new Date(generatedAt).toLocaleString()
    : null;

  if (variant === 'compact') {
    return (
      <span
        className={cn(
          'inline-flex items-center gap-1.5 rounded-full px-2.5 py-0.5 text-xs font-medium',
          'bg-info-100 text-info-700 dark:bg-info-700/20 dark:text-info-300',
          className
        )}
        title={`AI-generated by ${systemName}${confidencePct != null ? ` (${confidencePct}% confidence)` : ''}`}
      >
        <svg className="h-3 w-3" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
          <path d="M12 2a4 4 0 0 1 4 4v1a4 4 0 0 1-8 0V6a4 4 0 0 1 4-4z" />
          <path d="M16 15a4 4 0 0 0-8 0" />
          <circle cx="12" cy="20" r="2" />
        </svg>
        AI
        {confidencePct != null && <span className="opacity-75">{confidencePct}%</span>}
      </span>
    );
  }

  if (variant === 'banner') {
    return (
      <div
        className={cn(
          'rounded-lg border border-info-200 bg-info-50 p-4 dark:border-info-700/30 dark:bg-info-900/20',
          className
        )}
      >
        <div className="flex items-start gap-3">
          <div className="shrink-0 mt-0.5">
            <svg className="h-5 w-5 text-info-600" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
              <path d="M12 2a4 4 0 0 1 4 4v1a4 4 0 0 1-8 0V6a4 4 0 0 1 4-4z" />
              <path d="M16 15a4 4 0 0 0-8 0" />
              <circle cx="12" cy="20" r="2" />
            </svg>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-semibold text-info-900 dark:text-info-100">
              AI-Generated Content
            </p>
            <p className="text-sm text-info-700 dark:text-info-300 mt-0.5">
              This output was produced by <strong>{systemName}</strong>
              {modelVersion && <span className="text-info-500"> (v{modelVersion})</span>}
              {confidencePct != null && <span> with {confidencePct}% confidence</span>}
              {formattedDate && <span> on {formattedDate}</span>}.
            </p>
            {pendingReview && (
              <p className="text-xs text-warning-600 dark:text-warning-400 mt-1 font-medium">
                Pending human review
              </p>
            )}
            {reasoning && (
              <p className="text-xs text-info-600 dark:text-info-400 mt-2 leading-relaxed">{reasoning}</p>
            )}
          </div>
        </div>
      </div>
    );
  }

  // Default: inline
  return (
    <div
      className={cn(
        'rounded-md border border-gray-200 bg-gray-50 dark:border-gray-700 dark:bg-gray-800/50',
        className
      )}
    >
      <button
        onClick={() => setExpanded(!expanded)}
        className="flex w-full items-center gap-2 px-3 py-2 text-left text-xs text-gray-500 hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-200 transition-colors"
      >
        <svg className="h-3.5 w-3.5 text-info-500" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
          <path d="M12 2a4 4 0 0 1 4 4v1a4 4 0 0 1-8 0V6a4 4 0 0 1 4-4z" />
          <path d="M16 15a4 4 0 0 0-8 0" />
          <circle cx="12" cy="20" r="2" />
        </svg>
        <span className="font-medium">AI-generated by {systemName}</span>
        {confidencePct != null && (
          <span className="ml-auto text-gray-400">{confidencePct}%</span>
        )}
        {pendingReview && (
          <span className="rounded-full bg-warning-100 text-warning-700 px-1.5 py-0.5 text-[10px] font-semibold dark:bg-warning-700/20 dark:text-warning-300">
            Review needed
          </span>
        )}
        <svg
          className={cn('h-3 w-3 transition-transform', expanded && 'rotate-180')}
          viewBox="0 0 20 20"
          fill="currentColor"
        >
          <path fillRule="evenodd" d="M5.293 7.293a1 1 0 011.414 0L10 10.586l3.293-3.293a1 1 0 111.414 1.414l-4 4a1 1 0 01-1.414 0l-4-4a1 1 0 010-1.414z" clipRule="evenodd" />
        </svg>
      </button>
      {expanded && (
        <div className="border-t border-gray-200 dark:border-gray-700 px-3 py-2 text-xs text-gray-500 dark:text-gray-400 space-y-1">
          {modelVersion && <p>Model: {modelVersion}</p>}
          {formattedDate && <p>Generated: {formattedDate}</p>}
          {reasoning && <p className="leading-relaxed">{reasoning}</p>}
        </div>
      )}
    </div>
  );
}
