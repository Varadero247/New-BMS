'use client';

import * as React from 'react';
import { cn } from './utils';

export interface AIDisclosureProps {
  /** AI provider: claude, openai, grok */
  provider?: 'claude' | 'openai' | 'grok' | string;
  /** Model identifier e.g. "claude-sonnet-4-5" */
  model?: string;
  /** Analysis type e.g. "Risk Assessment" */
  analysisType?: string;
  /** Name of the AI system (legacy compat) */
  systemName?: string;
  /** Confidence score 0-1 (displayed as percentage) */
  confidence?: number;
  /** Model / version identifier (legacy compat) */
  modelVersion?: string;
  /** When the output was generated */
  generatedAt?: Date | string;
  /** Unique request ID linking to audit log */
  requestId?: string;
  /** Short explanation of how the AI produced this output */
  reasoning?: string;
  /** Whether the output is under human review */
  pendingReview?: boolean;
  /** Callback when user gives feedback */
  onFeedback?: (helpful: boolean) => void;
  /** Variant styling */
  variant?: 'inline' | 'banner' | 'compact';
  className?: string;
}

// ISO 42001 § 8.5 — AI output transparency label
export function AIDisclosure({
  provider,
  model,
  analysisType,
  systemName,
  confidence,
  modelVersion,
  generatedAt,
  requestId,
  reasoning,
  pendingReview,
  onFeedback,
  variant = 'inline',
  className,
}: AIDisclosureProps) {
  const [expanded, setExpanded] = React.useState(false);
  const [feedbackGiven, setFeedbackGiven] = React.useState<boolean | null>(null);
  const confidencePct = confidence != null ? Math.round(confidence * 100) : null;
  const displayName = systemName || provider || 'AI';
  const displayModel = model || modelVersion;

  const formattedDate = generatedAt ? new Date(generatedAt).toLocaleString() : null;

  const handleFeedback = (helpful: boolean) => {
    setFeedbackGiven(helpful);
    onFeedback?.(helpful);
  };

  if (variant === 'compact') {
    return (
      <span
        className={cn(
          'inline-flex items-center gap-1.5 rounded-full px-2.5 py-0.5 text-xs font-medium',
          'bg-brand-50 text-brand-700 dark:bg-brand-900/20 dark:text-brand-300',
          className
        )}
        title={`AI-generated by ${displayName}${confidencePct != null ? ` (${confidencePct}% confidence)` : ''}`}
      >
        <svg
          className="h-3 w-3"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
        >
          <path d="M9.813 15.904L9 18.75l-.813-2.846a4.5 4.5 0 00-3.09-3.09L2.25 12l2.846-.813a4.5 4.5 0 003.09-3.09L9 5.25l.813 2.846a4.5 4.5 0 003.09 3.09L15.75 12l-2.846.813a4.5 4.5 0 00-3.09 3.09z" />
        </svg>
        AI
        {confidencePct != null && <span className="opacity-75">{confidencePct}%</span>}
      </span>
    );
  }

  if (variant === 'banner') {
    return (
      <div
        className={cn(
          'rounded-lg border border-brand-100 bg-brand-50 p-4 dark:border-brand-800/30 dark:bg-brand-900/10',
          className
        )}
        role="status"
        aria-label="AI-generated content disclosure"
      >
        <div className="flex items-start gap-3">
          <div className="shrink-0 mt-0.5">
            <svg
              className="h-5 w-5 text-brand-500"
              viewBox="0 0 24 24"
              fill="none"
              stroke="currentColor"
              strokeWidth="2"
            >
              <path d="M9.813 15.904L9 18.75l-.813-2.846a4.5 4.5 0 00-3.09-3.09L2.25 12l2.846-.813a4.5 4.5 0 003.09-3.09L9 5.25l.813 2.846a4.5 4.5 0 003.09 3.09L15.75 12l-2.846.813a4.5 4.5 0 00-3.09 3.09z" />
            </svg>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-semibold text-brand-900 dark:text-brand-100">
              AI-generated
              {analysisType && <span> &middot; {analysisType}</span>}
              {provider && <span> &middot; {provider}</span>}
              {displayModel && <span className="text-brand-500"> &middot; {displayModel}</span>}
            </p>
            <p className="text-xs text-brand-700 dark:text-brand-300 mt-0.5">
              {formattedDate && <span>{formattedDate}</span>}
              {requestId && <span> &middot; ID: {requestId}</span>}
              {confidencePct != null && <span> &middot; {confidencePct}% confidence</span>}
            </p>
            <p className="text-xs text-warning-600 dark:text-warning-400 mt-1 font-medium">
              Review before acting. Human oversight required.
            </p>
            {pendingReview && (
              <p className="text-xs text-danger-600 dark:text-danger-400 mt-0.5 font-medium">
                Pending human review
              </p>
            )}
            {reasoning && (
              <p className="text-xs text-brand-600 dark:text-brand-400 mt-2 leading-relaxed">
                {reasoning}
              </p>
            )}
          </div>
          {/* Feedback buttons */}
          {onFeedback && (
            <div className="flex items-center gap-1 shrink-0">
              {feedbackGiven === null ? (
                <>
                  <button
                    type="button"
                    onClick={() => handleFeedback(true)}
                    className="p-1.5 rounded-md text-gray-400 hover:text-success-600 hover:bg-success-50 transition-colors"
                    aria-label="Mark as helpful"
                    title="Helpful"
                  >
                    <svg
                      className="h-4 w-4"
                      fill="none"
                      viewBox="0 0 24 24"
                      stroke="currentColor"
                      strokeWidth="2"
                    >
                      <path d="M6.633 10.25c.806 0 1.533-.446 2.031-1.08a9.041 9.041 0 012.861-2.4c.723-.384 1.35-.956 1.653-1.715a4.498 4.498 0 00.322-1.672V3a.75.75 0 01.75-.75 2.25 2.25 0 012.25 2.25c0 1.152-.26 2.243-.723 3.218-.266.558.107 1.282.725 1.282m0 0h3.126c1.026 0 1.945.694 2.054 1.715.045.422.068.85.068 1.285a11.95 11.95 0 01-2.649 7.521c-.388.482-.987.729-1.605.729H13.48c-.483 0-.964-.078-1.423-.23l-3.114-1.04a4.501 4.501 0 00-1.423-.23H5.904m7.72 2.72a4.5 4.5 0 01-4.5 4.5H5.25" />
                    </svg>
                  </button>
                  <button
                    type="button"
                    onClick={() => handleFeedback(false)}
                    className="p-1.5 rounded-md text-gray-400 hover:text-danger-600 hover:bg-danger-50 transition-colors"
                    aria-label="Mark as not helpful"
                    title="Not helpful"
                  >
                    <svg
                      className="h-4 w-4"
                      fill="none"
                      viewBox="0 0 24 24"
                      stroke="currentColor"
                      strokeWidth="2"
                    >
                      <path d="M7.498 15.25H4.372c-1.026 0-1.945-.694-2.054-1.715A12.134 12.134 0 012.25 12c0-2.848.992-5.464 2.649-7.521C5.287 3.997 5.886 3.75 6.504 3.75h4.016c.483 0 .964.078 1.423.23l3.114 1.04a4.5 4.5 0 001.423.23h1.294M7.498 15.25c.618 0 .991.724.725 1.282A7.471 7.471 0 007.5 19.5a2.25 2.25 0 002.25 2.25.75.75 0 00.75-.75v-.633c0-.573.11-1.14.322-1.672.304-.76.93-1.33 1.653-1.715a9.04 9.04 0 002.86-2.4c.498-.634 1.226-1.08 2.032-1.08h.384" />
                    </svg>
                  </button>
                </>
              ) : (
                <span className="text-xs text-gray-400">{feedbackGiven ? 'Thanks!' : 'Noted'}</span>
              )}
            </div>
          )}
        </div>
      </div>
    );
  }

  // Default: inline (collapsible)
  return (
    <div
      className={cn(
        'rounded-md border border-gray-200 bg-gray-50 dark:border-gray-700 dark:bg-gray-800/50',
        className
      )}
      role="status"
      aria-label="AI-generated content disclosure"
    >
      <button
        onClick={() => setExpanded(!expanded)}
        className="flex w-full items-center gap-2 px-3 py-2 text-left text-xs text-gray-500 hover:text-gray-700 dark:text-gray-400 dark:hover:text-gray-200 transition-colors"
      >
        <svg
          className="h-3.5 w-3.5 text-brand-500"
          viewBox="0 0 24 24"
          fill="none"
          stroke="currentColor"
          strokeWidth="2"
        >
          <path d="M9.813 15.904L9 18.75l-.813-2.846a4.5 4.5 0 00-3.09-3.09L2.25 12l2.846-.813a4.5 4.5 0 003.09-3.09L9 5.25l.813 2.846a4.5 4.5 0 003.09 3.09L15.75 12l-2.846.813a4.5 4.5 0 00-3.09 3.09z" />
        </svg>
        <span className="font-medium">AI-generated by {displayName}</span>
        {analysisType && <span className="text-gray-400">&middot; {analysisType}</span>}
        {confidencePct != null && <span className="ml-auto text-gray-400">{confidencePct}%</span>}
        {pendingReview && (
          <span className="rounded-full bg-warning-100 text-warning-700 px-1.5 py-0.5 text-[10px] font-semibold dark:bg-warning-700/20 dark:text-warning-300">
            Review needed
          </span>
        )}
        <svg
          className={cn('h-3 w-3 transition-transform', expanded && 'rotate-180')}
          viewBox="0 0 20 20"
          fill="currentColor"
        >
          <path
            fillRule="evenodd"
            d="M5.293 7.293a1 1 0 011.414 0L10 10.586l3.293-3.293a1 1 0 111.414 1.414l-4 4a1 1 0 01-1.414 0l-4-4a1 1 0 010-1.414z"
            clipRule="evenodd"
          />
        </svg>
      </button>
      {expanded && (
        <div className="border-t border-gray-200 dark:border-gray-700 px-3 py-2 text-xs text-gray-500 dark:text-gray-400 space-y-1">
          {provider && <p>Provider: {provider}</p>}
          {displayModel && <p>Model: {displayModel}</p>}
          {formattedDate && <p>Generated: {formattedDate}</p>}
          {requestId && <p>Request ID: {requestId}</p>}
          {reasoning && <p className="leading-relaxed">{reasoning}</p>}
          <p className="text-warning-600 dark:text-warning-400 font-medium mt-1">
            Review before acting. Human oversight required.
          </p>
          {onFeedback && feedbackGiven === null && (
            <div className="flex items-center gap-2 pt-1">
              <span>Was this helpful?</span>
              <button
                type="button"
                onClick={() => handleFeedback(true)}
                className="text-success-600 hover:underline"
              >
                Yes
              </button>
              <button
                type="button"
                onClick={() => handleFeedback(false)}
                className="text-danger-600 hover:underline"
              >
                No
              </button>
            </div>
          )}
        </div>
      )}
    </div>
  );
}
