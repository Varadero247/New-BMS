import type { FieldDefinition, TemplateDefinition } from './types';

/**
 * Renders a template to an inline-CSS HTML document.
 * Works without Puppeteer/Chromium — just returns an HTML string suitable for
 * browser download or server-side storage.
 */
export function renderTemplateToHtml(
  template: { code: string; name: string; description?: string | null; fields: FieldDefinition[] },
  filledData?: Record<string, unknown>
): string {
  const isPreview = !filledData;
  const title = `${template.code} — ${template.name}`;
  const rows = template.fields.map((f) => renderField(f, filledData)).join('\n');

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<title>${esc(title)}</title>
<style>
  *{box-sizing:border-box;margin:0;padding:0}
  body{font-family:'Segoe UI',Arial,sans-serif;color:#1f2937;margin:0;padding:40px;background:#fff}
  .header{border-bottom:3px solid #1a56db;padding-bottom:16px;margin-bottom:24px}
  .header h1{font-size:22px;color:#1a56db}
  .header .code{font-size:13px;color:#6b7280;margin-top:4px}
  .header .desc{font-size:14px;color:#374151;margin-top:8px}
  .section-title{font-size:16px;font-weight:700;color:#1e40af;margin:24px 0 12px;padding:8px 0;border-bottom:1px solid #dbeafe}
  .field{margin-bottom:16px;page-break-inside:avoid}
  .field-label{font-size:13px;font-weight:600;color:#374151;margin-bottom:4px}
  .field-label .req{color:#dc2626;margin-left:2px}
  .field-help{font-size:11px;color:#6b7280;margin-bottom:4px}
  .field-value{border:1px solid #d1d5db;border-radius:6px;padding:8px 12px;min-height:32px;font-size:14px;background:#f9fafb}
  .field-value.empty{color:#9ca3af;font-style:italic}
  .row{display:flex;gap:16px;flex-wrap:wrap}
  .row .field.half{flex:0 0 calc(50% - 8px)}
  .row .field.third{flex:0 0 calc(33.33% - 11px)}
  .row .field.full{flex:0 0 100%}
  table.field-table{width:100%;border-collapse:collapse;margin-top:4px}
  table.field-table th,table.field-table td{border:1px solid #d1d5db;padding:6px 10px;text-align:left;font-size:13px}
  table.field-table th{background:#f3f4f6;font-weight:600}
  .checkbox-row{display:flex;align-items:center;gap:8px}
  .checkbox-box{width:16px;height:16px;border:1px solid #6b7280;display:inline-block;text-align:center;font-size:12px;line-height:16px}
  .footer{margin-top:40px;padding-top:12px;border-top:1px solid #e5e7eb;font-size:11px;color:#9ca3af;display:flex;justify-content:space-between}
  @media print{body{padding:20px}@page{margin:15mm}}
</style>
</head>
<body>
<div class="header">
  <h1>${esc(template.name)}</h1>
  <div class="code">${esc(template.code)} • Version ${isPreview ? 'Preview' : 'Filled'}</div>
  ${template.description ? `<div class="desc">${esc(template.description)}</div>` : ''}
</div>
${rows}
<div class="footer">
  <span>Generated by Nexara IMS</span>
  <span>${new Date().toISOString().split('T')[0]}</span>
</div>
</body>
</html>`;
}

function renderField(field: FieldDefinition, data?: Record<string, unknown>): string {
  if (field.type === 'section') {
    return `<div class="section-title">${esc(field.label)}</div>`;
  }

  const value = data ? data[field.id] : undefined;
  const widthClass = field.width ?? 'full';
  const reqMark = field.required ? '<span class="req">*</span>' : '';
  const helpLine = field.helpText ? `<div class="field-help">${esc(field.helpText)}</div>` : '';

  if (field.type === 'table' && field.columns) {
    return renderTableField(field, data);
  }

  if (field.type === 'checkbox') {
    const checked = value === true || value === 'true';
    return `<div class="field ${widthClass}">
  <div class="checkbox-row">
    <span class="checkbox-box">${checked ? '✓' : '&nbsp;'}</span>
    <span class="field-label">${esc(field.label)}${reqMark}</span>
  </div>
  ${helpLine}
</div>`;
  }

  if (field.type === 'rating') {
    const rating = typeof value === 'number' ? value : 0;
    const stars = Array.from({ length: 5 }, (_, i) => (i < rating ? '★' : '☆')).join('');
    return `<div class="field ${widthClass}">
  <div class="field-label">${esc(field.label)}${reqMark}</div>
  ${helpLine}
  <div class="field-value">${stars} (${rating}/5)</div>
</div>`;
  }

  const displayValue = formatValue(field, value);
  const emptyClass = displayValue === '' ? ' empty' : '';

  return `<div class="field ${widthClass}">
  <div class="field-label">${esc(field.label)}${reqMark}</div>
  ${helpLine}
  <div class="field-value${emptyClass}">${displayValue || (field.placeholder ? esc(field.placeholder) : '—')}</div>
</div>`;
}

function renderTableField(field: FieldDefinition, data?: Record<string, unknown>): string {
  const cols = field.columns!;
  const rows = (data?.[field.id] as Record<string, unknown>[] | undefined) ?? [];
  const headerRow = cols.map((c) => `<th>${esc(c.label)}</th>`).join('');
  let bodyRows: string;
  if (rows.length > 0) {
    bodyRows = rows
      .map(
        (row) => `<tr>${cols.map((c) => `<td>${esc(String(row[c.id] ?? ''))}</td>`).join('')}</tr>`
      )
      .join('\n');
  } else {
    bodyRows = `<tr>${cols.map(() => '<td>&nbsp;</td>').join('')}</tr>`.repeat(3);
  }

  return `<div class="field full">
  <div class="field-label">${esc(field.label)}</div>
  ${field.helpText ? `<div class="field-help">${esc(field.helpText)}</div>` : ''}
  <table class="field-table">
    <thead><tr>${headerRow}</tr></thead>
    <tbody>${bodyRows}</tbody>
  </table>
</div>`;
}

function formatValue(field: FieldDefinition, value: unknown): string {
  if (value === undefined || value === null || value === '') return '';
  if (field.type === 'multiselect' && Array.isArray(value)) {
    return value
      .map((v) => {
        const opt = field.options?.find((o) => o.value === v);
        return esc(opt?.label ?? String(v));
      })
      .join(', ');
  }
  if ((field.type === 'select' || field.type === 'radio') && field.options) {
    const opt = field.options.find((o) => o.value === String(value));
    return esc(opt?.label ?? String(value));
  }
  if (field.type === 'date' || field.type === 'datetime') {
    try {
      const d = new Date(String(value));
      return esc(field.type === 'date' ? d.toLocaleDateString('en-GB') : d.toLocaleString('en-GB'));
    } catch {
      return esc(String(value));
    }
  }
  return esc(String(value));
}

function esc(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
