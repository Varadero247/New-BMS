describe('template seed data', () => {
  // Import from src directly so we test the actual data files
  const { allTemplates: ALL_TEMPLATES } = require('../src/seeds');

  it('exports at least 192 templates total', () => {
    expect(ALL_TEMPLATES.length).toBeGreaterThanOrEqual(192);
  });

  it('every template has a unique code', () => {
    const codes = ALL_TEMPLATES.map((t: { code: string }) => t.code);
    const unique = new Set(codes);
    expect(unique.size).toBe(codes.length);
  });

  it('every template has required fields: code, name, module, category', () => {
    for (const t of ALL_TEMPLATES) {
      expect(t).toHaveProperty('code');
      expect(t).toHaveProperty('name');
      expect(t).toHaveProperty('module');
      expect(t).toHaveProperty('category');
      expect(typeof t.code).toBe('string');
      expect(t.code.length).toBeGreaterThan(0);
    }
  });

  it('every template has at least 1 field', () => {
    for (const t of ALL_TEMPLATES) {
      expect(Array.isArray(t.fields)).toBe(true);
      expect(t.fields.length).toBeGreaterThan(0);
    }
  });

  it('every template code follows TPL-XXX-NNN pattern', () => {
    const pattern = /^TPL-[A-Z0-9]+-\d{3}$/;
    for (const t of ALL_TEMPLATES) {
      expect(t.code).toMatch(pattern);
    }
  });

  it('every field has id, label, and type', () => {
    for (const t of ALL_TEMPLATES) {
      for (const field of t.fields) {
        expect(field).toHaveProperty('id');
        expect(field).toHaveProperty('label');
        expect(field).toHaveProperty('type');
        expect(typeof field.id).toBe('string');
        expect(typeof field.label).toBe('string');
      }
    }
  });

  it('select/multiselect/radio fields have options array', () => {
    const OPTION_TYPES = ['select', 'multiselect', 'radio'];
    for (const t of ALL_TEMPLATES) {
      for (const field of t.fields) {
        if (OPTION_TYPES.includes(field.type)) {
          expect(Array.isArray(field.options)).toBe(true);
          expect(field.options.length).toBeGreaterThan(0);
        }
      }
    }
  });

  it('table fields have columns array', () => {
    for (const t of ALL_TEMPLATES) {
      for (const field of t.fields) {
        if (field.type === 'table') {
          expect(Array.isArray(field.columns)).toBe(true);
          expect(field.columns.length).toBeGreaterThan(0);
        }
      }
    }
  });

  it('every template renders to HTML without throwing', () => {
    for (const t of ALL_TEMPLATES) {
      expect(() => renderTemplateToHtml(t)).not.toThrow();
    }
  });

  it('every template exports to JSON without throwing', () => {
    for (const t of ALL_TEMPLATES) {
      expect(() => exportTemplate(t, undefined, 'json')).not.toThrow();
    }
  });
});


