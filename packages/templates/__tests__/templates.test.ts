import { renderTemplateToHtml } from '../src/renderer';
import { exportTemplate } from '../src/exporter';
import type { FieldDefinition } from '../src/types';

// ── Helpers ───────────────────────────────────────────────────────────────────

function makeTemplate(fields: FieldDefinition[], extra?: { description?: string }) {
  return {
    code: 'TPL-TEST-001',
    name: 'Test Template',
    description: extra?.description ?? null,
    fields,
  };
}

// ── renderTemplateToHtml ───────────────────────────────────────────────────────

describe('renderTemplateToHtml', () => {
  describe('document structure', () => {
    it('returns a complete HTML document', () => {
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).toContain('<!DOCTYPE html>');
      expect(html).toContain('<html lang="en">');
      expect(html).toContain('</html>');
    });

    it('includes template code and name in <title>', () => {
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).toContain('TPL-TEST-001 — Test Template');
    });

    it('renders template name in header h1', () => {
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).toContain('<h1>Test Template</h1>');
    });

    it('shows "Preview" in header when no filledData', () => {
      const html = renderTemplateToHtml(makeTemplate([]));
      expect(html).toContain('Version Preview');
    });

    it('shows "Filled" in header when filledData provided', () => {
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).toContain('Version Filled');
    });

    it('includes description when present', () => {
      const html = renderTemplateToHtml(makeTemplate([], { description: 'A helpful description' }), {});
      expect(html).toContain('A helpful description');
    });

    it('omits description block when null', () => {
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).not.toContain('class="desc"');
    });

    it('includes Nexara IMS footer', () => {
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).toContain('Generated by Nexara IMS');
    });

    it('includes today\'s date in footer (YYYY-MM-DD format)', () => {
      const today = new Date().toISOString().split('T')[0];
      const html = renderTemplateToHtml(makeTemplate([]), {});
      expect(html).toContain(today);
    });
  });

  describe('HTML escaping', () => {
    it('escapes ampersands in template name', () => {
      const tpl = { code: 'TPL-T-001', name: 'Health & Safety', description: null, fields: [] };
      const html = renderTemplateToHtml(tpl, {});
      expect(html).toContain('Health &amp; Safety');
    });

    it('escapes < and > in field labels', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: '<script>alert(1)</script>', type: 'text' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).not.toContain('<script>');
      expect(html).toContain('&lt;script&gt;');
    });

    it('escapes quotes in field values', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Name', type: 'text' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { f1: '"quoted"' });
      expect(html).toContain('&quot;quoted&quot;');
    });

    it('escapes XSS in description', () => {
      const tpl = { code: 'TPL-T-001', name: 'Test', description: '<img src=x onerror=alert(1)>', fields: [] };
      const html = renderTemplateToHtml(tpl, {});
      expect(html).not.toContain('<img');
      expect(html).toContain('&lt;img');
    });
  });

  describe('section fields', () => {
    it('renders section as a section-title div', () => {
      const fields: FieldDefinition[] = [{ id: 's1', label: 'Personal Details', type: 'section' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="section-title"');
      expect(html).toContain('Personal Details');
    });

    it('does not render field-label or field-value for section', () => {
      const fields: FieldDefinition[] = [{ id: 's1', label: 'Section Header', type: 'section' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      // Should not contain field wrapper for section
      expect(html.match(/class="field/g)).toBeNull();
    });
  });

  describe('text fields', () => {
    it('renders text field with filled value', () => {
      const fields: FieldDefinition[] = [{ id: 'name', label: 'Full Name', type: 'text' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { name: 'Alice Smith' });
      expect(html).toContain('Full Name');
      expect(html).toContain('Alice Smith');
    });

    it('shows placeholder when value is empty', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Notes', type: 'text', placeholder: 'Enter notes here' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('Enter notes here');
    });

    it('shows em dash when no value and no placeholder', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Field', type: 'text' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('—');
    });

    it('adds empty class when value is absent', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Field', type: 'text' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('field-value empty');
    });

    it('renders required marker (*) for required fields', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Required Field', type: 'text', required: true }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="req"');
      expect(html).toContain('*');
    });

    it('does not render required marker for optional fields', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Optional Field', type: 'text', required: false }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).not.toContain('class="req"');
    });

    it('renders helpText when present', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Field', type: 'text', helpText: 'Enter your name' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="field-help"');
      expect(html).toContain('Enter your name');
    });

    it('applies width class (half)', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Field', type: 'text', width: 'half' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="field half"');
    });

    it('applies width class (third)', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Field', type: 'text', width: 'third' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="field third"');
    });

    it('defaults width to full when not specified', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Field', type: 'text' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="field full"');
    });
  });

  describe('checkbox fields', () => {
    it('renders checked checkbox (boolean true)', () => {
      const fields: FieldDefinition[] = [{ id: 'cb', label: 'Agree', type: 'checkbox' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { cb: true });
      expect(html).toContain('✓');
    });

    it('renders checked checkbox (string "true")', () => {
      const fields: FieldDefinition[] = [{ id: 'cb', label: 'Agree', type: 'checkbox' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { cb: 'true' });
      expect(html).toContain('✓');
    });

    it('renders unchecked checkbox (false)', () => {
      const fields: FieldDefinition[] = [{ id: 'cb', label: 'Agree', type: 'checkbox' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { cb: false });
      expect(html).not.toContain('✓');
      expect(html).toContain('&nbsp;');
    });

    it('renders unchecked checkbox when value absent', () => {
      const fields: FieldDefinition[] = [{ id: 'cb', label: 'Agree', type: 'checkbox' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).not.toContain('✓');
    });

    it('renders checkbox-row layout', () => {
      const fields: FieldDefinition[] = [{ id: 'cb', label: 'Checkbox Label', type: 'checkbox' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('class="checkbox-row"');
      expect(html).toContain('class="checkbox-box"');
    });
  });

  describe('rating fields', () => {
    it('renders 3 filled stars and 2 empty for rating 3', () => {
      const fields: FieldDefinition[] = [{ id: 'r', label: 'Rating', type: 'rating' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { r: 3 });
      expect(html).toContain('★★★☆☆');
      expect(html).toContain('(3/5)');
    });

    it('renders 5 filled stars for rating 5', () => {
      const fields: FieldDefinition[] = [{ id: 'r', label: 'Rating', type: 'rating' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { r: 5 });
      expect(html).toContain('★★★★★');
      expect(html).toContain('(5/5)');
    });

    it('renders 0 filled stars for rating 0', () => {
      const fields: FieldDefinition[] = [{ id: 'r', label: 'Rating', type: 'rating' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { r: 0 });
      expect(html).toContain('☆☆☆☆☆');
      expect(html).toContain('(0/5)');
    });

    it('renders 0 filled stars when no value', () => {
      const fields: FieldDefinition[] = [{ id: 'r', label: 'Rating', type: 'rating' }];
      const html = renderTemplateToHtml(makeTemplate(fields), {});
      expect(html).toContain('(0/5)');
    });
  });

  describe('select and radio fields', () => {
    const OPTIONS = [
      { label: 'High Priority', value: 'high' },
      { label: 'Medium Priority', value: 'medium' },
      { label: 'Low Priority', value: 'low' },
    ];

    it('renders option label for select field', () => {
      const fields: FieldDefinition[] = [{ id: 'p', label: 'Priority', type: 'select', options: OPTIONS }];
      const html = renderTemplateToHtml(makeTemplate(fields), { p: 'high' });
      expect(html).toContain('High Priority');
    });

    it('falls back to raw value for unmatched select option', () => {
      const fields: FieldDefinition[] = [{ id: 'p', label: 'Priority', type: 'select', options: OPTIONS }];
      const html = renderTemplateToHtml(makeTemplate(fields), { p: 'unknown_value' });
      expect(html).toContain('unknown_value');
    });

    it('renders option label for radio field', () => {
      const fields: FieldDefinition[] = [{ id: 'p', label: 'Priority', type: 'radio', options: OPTIONS }];
      const html = renderTemplateToHtml(makeTemplate(fields), { p: 'low' });
      expect(html).toContain('Low Priority');
    });
  });

  describe('multiselect fields', () => {
    const OPTIONS = [
      { label: 'ISO 9001', value: 'iso9001' },
      { label: 'ISO 14001', value: 'iso14001' },
      { label: 'ISO 45001', value: 'iso45001' },
    ];

    it('renders comma-joined labels for selected values', () => {
      const fields: FieldDefinition[] = [{ id: 'certs', label: 'Certifications', type: 'multiselect', options: OPTIONS }];
      const html = renderTemplateToHtml(makeTemplate(fields), { certs: ['iso9001', 'iso14001'] });
      expect(html).toContain('ISO 9001');
      expect(html).toContain('ISO 14001');
      expect(html).toContain(', ');
    });

    it('uses raw value when option not found in multiselect', () => {
      const fields: FieldDefinition[] = [{ id: 'certs', label: 'Certifications', type: 'multiselect', options: OPTIONS }];
      const html = renderTemplateToHtml(makeTemplate(fields), { certs: ['custom_cert'] });
      expect(html).toContain('custom_cert');
    });

    it('renders empty multiselect gracefully', () => {
      const fields: FieldDefinition[] = [{ id: 'certs', label: 'Certifications', type: 'multiselect', options: OPTIONS }];
      const html = renderTemplateToHtml(makeTemplate(fields), { certs: [] });
      // Empty array → formatValue returns '' → shows placeholder or em-dash
      expect(html).toBeDefined();
    });
  });

  describe('date and datetime fields', () => {
    it('formats date value in en-GB locale', () => {
      const fields: FieldDefinition[] = [{ id: 'd', label: 'Date', type: 'date' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { d: '2026-01-15' });
      // en-GB format: DD/MM/YYYY or similar
      expect(html).toContain('2026');
      expect(html).toContain('01');
    });

    it('formats datetime value with time component', () => {
      const fields: FieldDefinition[] = [{ id: 'd', label: 'Date & Time', type: 'datetime' }];
      const html = renderTemplateToHtml(makeTemplate(fields), { d: '2026-01-15T10:30:00' });
      expect(html).toContain('2026');
    });

    it('falls back to raw string for invalid date', () => {
      const fields: FieldDefinition[] = [{ id: 'd', label: 'Date', type: 'date' }];
      // An invalid date string falls back gracefully
      const html = renderTemplateToHtml(makeTemplate(fields), { d: 'not-a-date' });
      expect(html).toBeDefined(); // should not throw
    });
  });

  describe('table fields', () => {
    const TABLE_FIELD: FieldDefinition = {
      id: 'items',
      label: 'Line Items',
      type: 'table',
      columns: [
        { id: 'sku', label: 'SKU', type: 'text' },
        { id: 'qty', label: 'Quantity', type: 'number' },
        { id: 'price', label: 'Price', type: 'number' },
      ],
    };

    it('renders column headers', () => {
      const html = renderTemplateToHtml(makeTemplate([TABLE_FIELD]), {});
      expect(html).toContain('<th>SKU</th>');
      expect(html).toContain('<th>Quantity</th>');
      expect(html).toContain('<th>Price</th>');
    });

    it('renders 3 empty rows when no data', () => {
      const html = renderTemplateToHtml(makeTemplate([TABLE_FIELD]), {});
      // 3 empty rows each with 3 empty <td>s
      const tdMatches = html.match(/<td>&nbsp;<\/td>/g);
      expect(tdMatches).toHaveLength(9); // 3 rows × 3 cols
    });

    it('renders data rows when provided', () => {
      const rows = [
        { sku: 'SKU-001', qty: 10, price: 25.5 },
        { sku: 'SKU-002', qty: 5, price: 12.0 },
      ];
      const html = renderTemplateToHtml(makeTemplate([TABLE_FIELD]), { items: rows });
      expect(html).toContain('SKU-001');
      expect(html).toContain('SKU-002');
      expect(html).toContain('10');
      expect(html).toContain('25.5');
    });

    it('renders field table CSS class', () => {
      const html = renderTemplateToHtml(makeTemplate([TABLE_FIELD]), {});
      expect(html).toContain('class="field-table"');
    });

    it('escapes cell values in table', () => {
      const rows = [{ sku: '<script>', qty: 1, price: 0 }];
      const html = renderTemplateToHtml(makeTemplate([TABLE_FIELD]), { items: rows });
      expect(html).not.toContain('<script>');
      expect(html).toContain('&lt;script&gt;');
    });
  });

  describe('preview mode (no filledData)', () => {
    it('renders fields without values in preview', () => {
      const fields: FieldDefinition[] = [{ id: 'f1', label: 'Name', type: 'text', placeholder: 'Enter name' }];
      const html = renderTemplateToHtml(makeTemplate(fields));
      expect(html).toContain('Version Preview');
      expect(html).toContain('Enter name');
    });

    it('renders table with empty rows in preview', () => {
      const fields: FieldDefinition[] = [{
        id: 'tbl',
        label: 'Table',
        type: 'table',
        columns: [{ id: 'col1', label: 'Col 1', type: 'text' }],
      }];
      const html = renderTemplateToHtml(makeTemplate(fields));
      expect(html).toContain('&nbsp;');
    });
  });
});

// ── exportTemplate ─────────────────────────────────────────────────────────────

describe('exportTemplate', () => {
  const BASE_TEMPLATE = {
    code: 'TPL-HS-001',
    name: 'Risk Assessment Form',
    description: 'Standard risk assessment',
    fields: [{ id: 'hazard', label: 'Hazard Description', type: 'text' as const }],
    version: 2,
  };

  describe('HTML export (default)', () => {
    it('returns html mime type', () => {
      const result = exportTemplate(BASE_TEMPLATE, {});
      expect(result.mimeType).toBe('text/html');
    });

    it('returns .html filename based on template code', () => {
      const result = exportTemplate(BASE_TEMPLATE, {});
      expect(result.filename).toBe('tpl-hs-001.html');
    });

    it('content is a valid HTML document', () => {
      const result = exportTemplate(BASE_TEMPLATE, {});
      expect(result.content).toContain('<!DOCTYPE html>');
      expect(result.content).toContain('</html>');
    });

    it('html format is explicit default', () => {
      const explicit = exportTemplate(BASE_TEMPLATE, {}, 'html');
      const implicit = exportTemplate(BASE_TEMPLATE, {});
      expect(explicit.mimeType).toBe(implicit.mimeType);
      expect(explicit.filename).toBe(implicit.filename);
    });
  });

  describe('JSON export', () => {
    it('returns json mime type', () => {
      const result = exportTemplate(BASE_TEMPLATE, {}, 'json');
      expect(result.mimeType).toBe('application/json');
    });

    it('returns .json filename based on template code', () => {
      const result = exportTemplate(BASE_TEMPLATE, {}, 'json');
      expect(result.filename).toBe('tpl-hs-001.json');
    });

    it('content is valid parseable JSON', () => {
      const result = exportTemplate(BASE_TEMPLATE, { hazard: 'Chemical spill' }, 'json');
      const parsed = JSON.parse(result.content);
      expect(parsed).toBeDefined();
    });

    it('JSON payload contains code, name, description', () => {
      const result = exportTemplate(BASE_TEMPLATE, {}, 'json');
      const parsed = JSON.parse(result.content);
      expect(parsed.code).toBe('TPL-HS-001');
      expect(parsed.name).toBe('Risk Assessment Form');
      expect(parsed.description).toBe('Standard risk assessment');
    });

    it('JSON payload contains version', () => {
      const result = exportTemplate(BASE_TEMPLATE, {}, 'json');
      const parsed = JSON.parse(result.content);
      expect(parsed.version).toBe(2);
    });

    it('JSON payload defaults version to 1 when not provided', () => {
      const tpl = { code: 'TPL-T-001', name: 'Test', description: null, fields: [] };
      const result = exportTemplate(tpl, {}, 'json');
      const parsed = JSON.parse(result.content);
      expect(parsed.version).toBe(1);
    });

    it('JSON payload includes filledData', () => {
      const result = exportTemplate(BASE_TEMPLATE, { hazard: 'Oil leak' }, 'json');
      const parsed = JSON.parse(result.content);
      expect(parsed.filledData.hazard).toBe('Oil leak');
    });

    it('JSON payload sets filledData to null when undefined', () => {
      const result = exportTemplate(BASE_TEMPLATE, undefined, 'json');
      const parsed = JSON.parse(result.content);
      expect(parsed.filledData).toBeNull();
    });

    it('JSON payload contains exportedAt ISO timestamp', () => {
      const before = new Date().toISOString();
      const result = exportTemplate(BASE_TEMPLATE, {}, 'json');
      const after = new Date().toISOString();
      const parsed = JSON.parse(result.content);
      expect(parsed.exportedAt >= before).toBe(true);
      expect(parsed.exportedAt <= after).toBe(true);
    });

    it('JSON payload contains fields array', () => {
      const result = exportTemplate(BASE_TEMPLATE, {}, 'json');
      const parsed = JSON.parse(result.content);
      expect(Array.isArray(parsed.fields)).toBe(true);
      expect(parsed.fields[0].id).toBe('hazard');
    });
  });

  describe('slug generation', () => {
    it('lowercases the template code', () => {
      const tpl = { code: 'TPL-QUALITY-001', name: 'Test', description: null, fields: [] };
      const result = exportTemplate(tpl, {});
      expect(result.filename).toBe('tpl-quality-001.html');
    });

    it('replaces special chars with hyphens in slug', () => {
      const tpl = { code: 'MY TEMPLATE #1', name: 'Test', description: null, fields: [] };
      const result = exportTemplate(tpl, {}, 'json');
      expect(result.filename).toBe('my-template-1.json');
    });

    it('handles already-hyphenated codes', () => {
      const tpl = { code: 'TPL-HS-042', name: 'Test', description: null, fields: [] };
      const result = exportTemplate(tpl, {});
      expect(result.filename).toBe('tpl-hs-042.html');
    });
  });
});

// ── Seed data integrity ────────────────────────────────────────────────────────

describe('template seed data', () => {
  // Import from src directly so we test the actual data files
  const { allTemplates: ALL_TEMPLATES } = require('../src/seeds');

  it('exports at least 192 templates total', () => {
    expect(ALL_TEMPLATES.length).toBeGreaterThanOrEqual(192);
  });

  it('every template has a unique code', () => {
    const codes = ALL_TEMPLATES.map((t: { code: string }) => t.code);
    const unique = new Set(codes);
    expect(unique.size).toBe(codes.length);
  });

  it('every template has required fields: code, name, module, category', () => {
    for (const t of ALL_TEMPLATES) {
      expect(t).toHaveProperty('code');
      expect(t).toHaveProperty('name');
      expect(t).toHaveProperty('module');
      expect(t).toHaveProperty('category');
      expect(typeof t.code).toBe('string');
      expect(t.code.length).toBeGreaterThan(0);
    }
  });

  it('every template has at least 1 field', () => {
    for (const t of ALL_TEMPLATES) {
      expect(Array.isArray(t.fields)).toBe(true);
      expect(t.fields.length).toBeGreaterThan(0);
    }
  });

  it('every template code follows TPL-XXX-NNN pattern', () => {
    const pattern = /^TPL-[A-Z0-9]+-\d{3}$/;
    for (const t of ALL_TEMPLATES) {
      expect(t.code).toMatch(pattern);
    }
  });

  it('every field has id, label, and type', () => {
    for (const t of ALL_TEMPLATES) {
      for (const field of t.fields) {
        expect(field).toHaveProperty('id');
        expect(field).toHaveProperty('label');
        expect(field).toHaveProperty('type');
        expect(typeof field.id).toBe('string');
        expect(typeof field.label).toBe('string');
      }
    }
  });

  it('select/multiselect/radio fields have options array', () => {
    const OPTION_TYPES = ['select', 'multiselect', 'radio'];
    for (const t of ALL_TEMPLATES) {
      for (const field of t.fields) {
        if (OPTION_TYPES.includes(field.type)) {
          expect(Array.isArray(field.options)).toBe(true);
          expect(field.options.length).toBeGreaterThan(0);
        }
      }
    }
  });

  it('table fields have columns array', () => {
    for (const t of ALL_TEMPLATES) {
      for (const field of t.fields) {
        if (field.type === 'table') {
          expect(Array.isArray(field.columns)).toBe(true);
          expect(field.columns.length).toBeGreaterThan(0);
        }
      }
    }
  });

  it('every template renders to HTML without throwing', () => {
    for (const t of ALL_TEMPLATES) {
      expect(() => renderTemplateToHtml(t)).not.toThrow();
    }
  });

  it('every template exports to JSON without throwing', () => {
    for (const t of ALL_TEMPLATES) {
      expect(() => exportTemplate(t, undefined, 'json')).not.toThrow();
    }
  });
});


