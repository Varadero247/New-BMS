interface ParetoEntry {
  category: string;
  count: number;
  cumulative: number;
}

function buildParetoDatasets(data: ParetoEntry[]) {
  return {
    labels: data.map((d) => d.category),
    countData: data.map((d) => d.count),
    cumulativeData: data.map((d) => d.cumulative),
  };
}

