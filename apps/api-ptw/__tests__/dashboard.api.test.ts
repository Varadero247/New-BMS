    expect(res.status).toBe(200);
    expect(res.body.success).toBe(true);
    expect(res.body.data.totalPermits).toBe(10);
    expect(res.body.data.totalMethodStatements).toBe(5);
    expect(res.body.data.totalToolboxTalks).toBe(3);
