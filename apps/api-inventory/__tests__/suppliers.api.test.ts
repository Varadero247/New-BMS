      const response = await request(app)
        .post('/api/suppliers')
        .set('Authorization', 'Bearer token')
        .send({ name: 'No Code' });

      expect(response.status).toBe(400);
      expect(response.body.error.code).toBe('VALIDATION_ERROR');
