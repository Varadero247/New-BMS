import { Router, Response } from 'express';
import { prisma } from '@ims/database';
import { authenticate, type AuthRequest } from '@ims/auth';
import { createLogger } from '@ims/monitoring';
import { z } from 'zod';

const logger = createLogger('reports');
const router = Router();

// All report routes require authentication
router.use(authenticate);

// ---------------------------------------------------------------------------
// Validation schemas
// ---------------------------------------------------------------------------
const managementReviewSchema = z.object({
  period: z.string().min(1, 'Period is required').max(50), // e.g., "2026-Q1"
  title: z.string().min(1).max(200).optional(),
  includeRisks: z.boolean().optional().default(true),
  includeIncidents: z.boolean().optional().default(true),
  includeActions: z.boolean().optional().default(true),
  includeCompliance: z.boolean().optional().default(true),
});

const kpiPackSchema = z.object({
  period: z.string().min(1, 'Period is required').max(50),
  modules: z.array(z.string()).optional(),
  title: z.string().min(1).max(200).optional(),
});

const complianceSummarySchema = z.object({
  standards: z.array(z.string()).min(1, 'At least one standard is required'),
  title: z.string().min(1).max(200).optional(),
  includeGaps: z.boolean().optional().default(true),
  includeActions: z.boolean().optional().default(true),
});

// ---------------------------------------------------------------------------
// Helper: Generate HTML template for a report
// ---------------------------------------------------------------------------
function generateHtmlTemplate(title: string, content: Record<string, any>): string {
  const sections = Object.entries(content)
    .map(([key, value]) => {
      const sectionTitle = key.replace(/([A-Z])/g, ' $1').replace(/^./, (s) => s.toUpperCase());
      let body: string;
      if (Array.isArray(value)) {
        if (value.length === 0) {
          body = '<p>No data available.</p>';
        } else {
          const headers = Object.keys(value[0]);
          body = `<table><thead><tr>${headers.map((h) => `<th>${h}</th>`).join('')}</tr></thead><tbody>${value.map((row) => `<tr>${headers.map((h) => `<td>${row[h] ?? ''}</td>`).join('')}</tr>`).join('')}</tbody></table>`;
        }
      } else if (typeof value === 'object' && value !== null) {
        body = `<dl>${Object.entries(value).map(([k, v]) => `<dt>${k}</dt><dd>${v}</dd>`).join('')}</dl>`;
      } else {
        body = `<p>${value}</p>`;
      }
      return `<section><h2>${sectionTitle}</h2>${body}</section>`;
    })
    .join('\n');

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <title>${title}</title>
  <style>
    body { font-family: Arial, sans-serif; margin: 40px; color: #333; }
    h1 { color: #1a56db; border-bottom: 2px solid #1a56db; padding-bottom: 10px; }
    h2 { color: #374151; margin-top: 30px; }
    table { width: 100%; border-collapse: collapse; margin: 15px 0; }
    th, td { border: 1px solid #d1d5db; padding: 8px 12px; text-align: left; }
    th { background: #f3f4f6; font-weight: 600; }
    tr:nth-child(even) { background: #f9fafb; }
    dl { display: grid; grid-template-columns: 200px 1fr; gap: 8px; }
    dt { font-weight: 600; }
    .footer { margin-top: 40px; font-size: 12px; color: #6b7280; border-top: 1px solid #e5e7eb; padding-top: 10px; }
  </style>
</head>
<body>
  <h1>${title}</h1>
  <p><strong>Generated:</strong> ${new Date().toISOString()}</p>
  ${sections}
  <div class="footer">
    <p>Generated by IMS Auto-Report System. This document is confidential.</p>
  </div>
</body>
</html>`;
}

