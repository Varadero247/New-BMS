// POST /compliance-summary — Multi-standard compliance summary
// ---------------------------------------------------------------------------
router.post('/compliance-summary', async (req: AuthRequest, res: Response) => {
  try {
    const data = complianceSummarySchema.parse(req.body);
    const reportTitle = data.title || `Compliance Summary — ${data.standards.join(', ')}`;

    // Fetch compliance scores for requested standards
    const complianceScores = await prisma.complianceScore.findMany({
      where: {
        standard: { in: data.standards as any[] },
      },
    });

    const reportContent: Record<string, any> = {
      overview: {
        standards: data.standards,
        generatedAt: new Date().toISOString(),
        generatedBy: req.user?.email || req.user?.id,
      },
      complianceScores: complianceScores.map((cs) => ({
        standard: cs.standard,
        overallScore: cs.overallScore,
        riskScore: cs.riskScore,
        incidentScore: cs.incidentScore,
        legalScore: cs.legalScore,
        objectiveScore: cs.objectiveScore,
        actionScore: cs.actionScore,
        trainingScore: cs.trainingScore,
        documentScore: cs.documentScore,
        totalItems: cs.totalItems,
        compliantItems: cs.compliantItems,
        calculatedAt: cs.calculatedAt,
      })),
    };

    if (data.includeGaps) {
      // Identify gaps — standards with low scores
      const gaps = complianceScores
        .filter((cs) => cs.overallScore < 80)
        .map((cs) => ({
          standard: cs.standard,
          overallScore: cs.overallScore,
          weakestArea: [
            { area: 'risk', score: cs.riskScore },
            { area: 'incident', score: cs.incidentScore },
            { area: 'legal', score: cs.legalScore },
            { area: 'objective', score: cs.objectiveScore },
            { area: 'action', score: cs.actionScore },
          ]
            .filter((a) => a.score !== null)
            .sort((a, b) => (a.score || 0) - (b.score || 0))[0]?.area || 'unknown',
        }));

      reportContent.complianceGaps = gaps;
    }

    if (data.includeActions) {
      // Get open actions across requested standards
      const standardEnums = data.standards.filter((s) =>
        ['ISO_45001', 'ISO_14001', 'ISO_9001'].includes(s)
      );

      if (standardEnums.length > 0) {
        const openActions = await prisma.action.findMany({
          where: {
            standard: { in: standardEnums as any[] },
            status: { in: ['OPEN', 'IN_PROGRESS', 'OVERDUE'] },
            deletedAt: null,
          },
          orderBy: { dueDate: 'asc' },
          take: 50,
          select: {
            id: true,
            title: true,
            standard: true,
            type: true,
            priority: true,
            status: true,
            dueDate: true,
          },
        });

        reportContent.openActions = {
          count: openActions.length,
          records: openActions,
        };
      }
    }

    const htmlTemplate = generateHtmlTemplate(reportTitle, reportContent);

    const report = await prisma.generatedReport.create({
      data: {
        title: reportTitle,
        type: 'COMPLIANCE_SUMMARY',
        parameters: data as any,
        content: reportContent as any,
        htmlTemplate,
        format: 'JSON',
        generatedBy: req.user!.id,
      },
    });

    logger.info('Compliance summary report generated', {
      reportId: report.id,
      standards: data.standards,
      generatedBy: req.user?.id,
    });

    res.status(201).json({
      success: true,
      data: {
        id: report.id,
        title: report.title,
        type: report.type,
        format: report.format,
        content: reportContent,
        htmlTemplate,
        createdAt: report.createdAt,
      },
    });
  } catch (error) {
    if (error instanceof z.ZodError) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'VALIDATION_ERROR',
          message: 'Invalid input',
          fields: error.errors.map((e) => ({ path: e.path.join('.'), message: e.message })),
        },
      });
    }
    logger.error('Failed to generate compliance summary', { error: error instanceof Error ? error.message : String(error) });
    res.status(500).json({
      success: false,
      error: { code: 'INTERNAL_ERROR', message: 'Failed to generate compliance summary' },
    });
  }
});

