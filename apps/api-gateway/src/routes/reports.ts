// POST /management-review/:module — Generate management review report
// ---------------------------------------------------------------------------
router.post('/management-review/:module', async (req: AuthRequest, res: Response) => {
  try {
    const { module } = req.params;
    const data = managementReviewSchema.parse(req.body);

    const validModules = [
      'health-safety', 'environment', 'quality',
      'automotive', 'medical', 'aerospace',
    ];

    if (!validModules.includes(module)) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'INVALID_MODULE',
          message: `Invalid module '${module}'. Valid: ${validModules.join(', ')}`,
        },
      });
    }

    // Map module to ISO standard enum
    const standardMap: Record<string, string> = {
      'health-safety': 'ISO_45001',
      'environment': 'ISO_14001',
      'quality': 'ISO_9001',
    };

    const standard = standardMap[module];
    const reportTitle = data.title || `Management Review Report — ${module.toUpperCase()} (${data.period})`;

    // Gather data based on flags
    const reportContent: Record<string, any> = {
      overview: {
        module,
        period: data.period,
        generatedAt: new Date().toISOString(),
        generatedBy: req.user?.email || req.user?.id,
      },
    };

    if (data.includeRisks && standard) {
      const risks = await prisma.risk.findMany({
        where: { standard: standard as any, deletedAt: null },
        orderBy: { createdAt: 'desc' },
        take: 50,
        select: {
          id: true,
          title: true,
          riskLevel: true,
          status: true,
          riskScore: true,
          createdAt: true,
        },
      });

      const riskSummary = {
        total: risks.length,
        byLevel: {
          CRITICAL: risks.filter((r) => r.riskLevel === 'CRITICAL').length,
          HIGH: risks.filter((r) => r.riskLevel === 'HIGH').length,
          MEDIUM: risks.filter((r) => r.riskLevel === 'MEDIUM').length,
          LOW: risks.filter((r) => r.riskLevel === 'LOW').length,
        },
        byStatus: {
          ACTIVE: risks.filter((r) => r.status === 'ACTIVE').length,
          MITIGATED: risks.filter((r) => r.status === 'MITIGATED').length,
          CLOSED: risks.filter((r) => r.status === 'CLOSED').length,
        },
      };

      reportContent.risks = { summary: riskSummary, records: risks };
    }

    if (data.includeIncidents && standard) {
      const incidents = await prisma.incident.findMany({
        where: { standard: standard as any, deletedAt: null },
        orderBy: { dateOccurred: 'desc' },
        take: 50,
        select: {
          id: true,
          title: true,
          type: true,
          severity: true,
          status: true,
          dateOccurred: true,
        },
      });

      const incidentSummary = {
        total: incidents.length,
        bySeverity: {
          CRITICAL: incidents.filter((i) => i.severity === 'CRITICAL').length,
          MAJOR: incidents.filter((i) => i.severity === 'MAJOR').length,
          MODERATE: incidents.filter((i) => i.severity === 'MODERATE').length,
          MINOR: incidents.filter((i) => i.severity === 'MINOR').length,
        },
        open: incidents.filter((i) => i.status !== 'CLOSED').length,
        closed: incidents.filter((i) => i.status === 'CLOSED').length,
      };

      reportContent.incidents = { summary: incidentSummary, records: incidents };
    }

    if (data.includeActions && standard) {
      const actions = await prisma.action.findMany({
        where: { standard: standard as any, deletedAt: null },
        orderBy: { dueDate: 'asc' },
        take: 50,
        select: {
          id: true,
          title: true,
          type: true,
          priority: true,
          status: true,
          dueDate: true,
        },
      });

      const now = new Date();
      const actionSummary = {
        total: actions.length,
        overdue: actions.filter((a) => a.status !== 'COMPLETED' && a.status !== 'CANCELLED' && new Date(a.dueDate) < now).length,
        completed: actions.filter((a) => a.status === 'COMPLETED').length,
        inProgress: actions.filter((a) => a.status === 'IN_PROGRESS').length,
        open: actions.filter((a) => a.status === 'OPEN').length,
      };

      reportContent.actions = { summary: actionSummary, records: actions };
    }

    if (data.includeCompliance && standard) {
      const complianceScore = await prisma.complianceScore.findUnique({
        where: { standard: standard as any },
      });

      reportContent.compliance = complianceScore || { message: 'No compliance score data available' };
    }

    const htmlTemplate = generateHtmlTemplate(reportTitle, reportContent);

    // Save to database
    const report = await prisma.generatedReport.create({
      data: {
        title: reportTitle,
        type: 'MANAGEMENT_REVIEW',
        module,
        parameters: data as any,
        content: reportContent as any,
        htmlTemplate,
        format: 'JSON',
        generatedBy: req.user!.id,
      },
    });

    logger.info('Management review report generated', {
      reportId: report.id,
      module,
      period: data.period,
      generatedBy: req.user?.id,
    });

    res.status(201).json({
      success: true,
      data: {
        id: report.id,
        title: report.title,
        type: report.type,
        module: report.module,
        format: report.format,
        content: reportContent,
        htmlTemplate,
        createdAt: report.createdAt,
      },
    });
  } catch (error) {
    if (error instanceof z.ZodError) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'VALIDATION_ERROR',
          message: 'Invalid input',
          fields: error.errors.map((e) => ({ path: e.path.join('.'), message: e.message })),
        },
      });
    }
    logger.error('Failed to generate management review report', { error: error instanceof Error ? error.message : String(error) });
    res.status(500).json({
      success: false,
      error: { code: 'INTERNAL_ERROR', message: 'Failed to generate management review report' },
    });
  }
});

