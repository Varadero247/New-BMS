// POST /audit/:auditId — Generate audit report
// ---------------------------------------------------------------------------
router.post('/audit/:auditId', async (req: AuthRequest, res: Response) => {
  try {
    const { auditId } = req.params;
    const title = req.body.title || `Audit Report — ${auditId}`;

    // Fetch audit-related data from the audit logs
    const auditEntries = await prisma.auditLog.findMany({
      where: { entityId: auditId },
      orderBy: { createdAt: 'asc' },
      take: 200,
    });

    // Fetch enhanced audit trail entries if available
    const enhancedEntries = await prisma.enhancedAuditTrail.findMany({
      where: { resourceId: auditId },
      orderBy: { createdAt: 'asc' },
      take: 200,
    });

    const reportContent: Record<string, any> = {
      overview: {
        auditId,
        generatedAt: new Date().toISOString(),
        generatedBy: req.user?.email || req.user?.id,
      },
      auditTrail: {
        entryCount: auditEntries.length,
        entries: auditEntries.map((e) => ({
          id: e.id,
          action: e.action,
          entity: e.entity,
          createdAt: e.createdAt,
          userId: e.userId,
        })),
      },
      enhancedAuditTrail: {
        entryCount: enhancedEntries.length,
        entries: enhancedEntries.map((e) => ({
          id: e.id,
          action: e.action,
          resourceType: e.resourceType,
          userFullName: e.userFullName,
          createdAt: e.createdAt,
        })),
      },
    };

    const htmlTemplate = generateHtmlTemplate(title, reportContent);

    const report = await prisma.generatedReport.create({
      data: {
        title,
        type: 'AUDIT',
        parameters: { auditId } as any,
        content: reportContent as any,
        htmlTemplate,
        format: 'JSON',
        generatedBy: req.user!.id,
      },
    });

    logger.info('Audit report generated', {
      reportId: report.id,
      auditId,
      generatedBy: req.user?.id,
    });

    res.status(201).json({
      success: true,
      data: {
        id: report.id,
        title: report.title,
        type: report.type,
        format: report.format,
        content: reportContent,
        htmlTemplate,
        createdAt: report.createdAt,
      },
    });
  } catch (error) {
    logger.error('Failed to generate audit report', { error: error instanceof Error ? error.message : String(error) });
    res.status(500).json({
      success: false,
      error: { code: 'INTERNAL_ERROR', message: 'Failed to generate audit report' },
    });
  }
});

