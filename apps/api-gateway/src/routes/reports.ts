// POST /kpi-pack — Generate monthly KPI pack
// ---------------------------------------------------------------------------
router.post('/kpi-pack', async (req: AuthRequest, res: Response) => {
  try {
    const data = kpiPackSchema.parse(req.body);
    const reportTitle = data.title || `KPI Pack — ${data.period}`;

    // Parse period to year/month if possible (e.g., "2026-01")
    const periodParts = data.period.match(/^(\d{4})-(\d{2})$/);
    const year = periodParts ? parseInt(periodParts[1], 10) : new Date().getFullYear();
    const month = periodParts ? parseInt(periodParts[2], 10) : new Date().getMonth() + 1;

    // Fetch monthly trends
    const trends = await prisma.monthlyTrend.findMany({
      where: { year, month },
      orderBy: [{ standard: 'asc' }, { metric: 'asc' }],
    });

    // Fetch compliance scores for all standards
    const complianceScores = await prisma.complianceScore.findMany({
      orderBy: { standard: 'asc' },
    });

    // Fetch action stats
    const now = new Date();
    const allActions = await prisma.action.findMany({
      where: { deletedAt: null },
      select: { status: true, dueDate: true, standard: true },
    });

    const actionKPIs = {
      total: allActions.length,
      completedOnTime: allActions.filter((a) => a.status === 'COMPLETED').length,
      overdue: allActions.filter((a) => a.status !== 'COMPLETED' && a.status !== 'CANCELLED' && new Date(a.dueDate) < now).length,
      completionRate: allActions.length > 0
        ? Math.round((allActions.filter((a) => a.status === 'COMPLETED').length / allActions.length) * 100)
        : 0,
    };

    // Fetch incident stats
    const allIncidents = await prisma.incident.findMany({
      where: { deletedAt: null },
      select: { status: true, severity: true, standard: true, dateOccurred: true },
    });

    const incidentKPIs = {
      total: allIncidents.length,
      open: allIncidents.filter((i) => i.status !== 'CLOSED').length,
      closed: allIncidents.filter((i) => i.status === 'CLOSED').length,
      critical: allIncidents.filter((i) => i.severity === 'CRITICAL').length,
      closeRate: allIncidents.length > 0
        ? Math.round((allIncidents.filter((i) => i.status === 'CLOSED').length / allIncidents.length) * 100)
        : 0,
    };

    const reportContent: Record<string, any> = {
      overview: {
        period: data.period,
        year,
        month,
        generatedAt: new Date().toISOString(),
        generatedBy: req.user?.email || req.user?.id,
      },
      complianceScores: complianceScores.map((cs) => ({
        standard: cs.standard,
        overallScore: cs.overallScore,
        riskScore: cs.riskScore,
        incidentScore: cs.incidentScore,
        actionScore: cs.actionScore,
        calculatedAt: cs.calculatedAt,
      })),
      monthlyTrends: trends.map((t) => ({
        standard: t.standard,
        metric: t.metric,
        value: t.value,
      })),
      actionKPIs,
      incidentKPIs,
    };

    const htmlTemplate = generateHtmlTemplate(reportTitle, reportContent);

    const report = await prisma.generatedReport.create({
      data: {
        title: reportTitle,
        type: 'KPI_PACK',
        parameters: data as any,
        content: reportContent as any,
        htmlTemplate,
        format: 'JSON',
        generatedBy: req.user!.id,
      },
    });

    logger.info('KPI pack generated', {
      reportId: report.id,
      period: data.period,
      generatedBy: req.user?.id,
    });

    res.status(201).json({
      success: true,
      data: {
        id: report.id,
        title: report.title,
        type: report.type,
        format: report.format,
        content: reportContent,
        htmlTemplate,
        createdAt: report.createdAt,
      },
    });
  } catch (error) {
    if (error instanceof z.ZodError) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'VALIDATION_ERROR',
          message: 'Invalid input',
          fields: error.errors.map((e) => ({ path: e.path.join('.'), message: e.message })),
        },
      });
    }
    logger.error('Failed to generate KPI pack', { error: error instanceof Error ? error.message : String(error) });
    res.status(500).json({
      success: false,
      error: { code: 'INTERNAL_ERROR', message: 'Failed to generate KPI pack' },
    });
  }
});

