        id: '1e000000-0000-4000-a000-000000000001',
        employeeId: '2a000000-0000-4000-a000-000000000001',
        documentType: 'CONTRACT',
        title: 'Employment Contract',
        fileName: 'contract.pdf',
        fileUrl: 'https://files.example.com/contract.pdf',
