    refNumber: 'ISI-2602-5678',
    title: 'Phishing attack on finance team',
    description: 'Multiple finance team members received targeted phishing emails',
    type: 'PHISHING',
    severity: 'HIGH',
    status: 'REPORTED',
    affectedSystems: ['email'],
    affectedAssetIds: [],
